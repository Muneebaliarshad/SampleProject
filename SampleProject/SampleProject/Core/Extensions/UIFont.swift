import UIKit

extension UIFont {
    
    final class func HelveticaNeueLight(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-Light", size: size)!
    }
    
    final class func HelveticaNeueMedium(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-Medium", size: size)!
    }
    
    final class func HelveticaNeueBold(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "HelveticaNeue-Bold", size: size)!
    }
    
}
